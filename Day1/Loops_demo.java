import java.util.*;
public class Loops_demo {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int no = sc.nextInt();


        // While loop
        int i = 1;
        while(i<=no){
            System.out.println("Hello");
            i++;
        }
        System.out.println("Bye Thankyou");
    }
}
